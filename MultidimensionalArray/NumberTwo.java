public class NumberTwo {
    public static void main(String[] args) {
        int num [] = new int[5];
        double number[] = new double[5];
        char character[] = new char[5];
        boolean tf [] = new boolean[1];
        String words[] = new String[1];

        for (int n : num){
        System.out.print(n);
        }
        
        System.out.println("");

        for (double d : number){
        System.out.print(d);
        }
        
        System.out.println("");

        for (char c : character){
        System.out.print(c);
        }

        System.out.println("");

        for (boolean t : tf){
        System.out.print(t);
        }

        System.out.println("");

        for (String str : words){
        System.out.print(str);
        }

    }
}
